def tip_for_week(week: int) -> str:
    if week < 1:
        return "Укажите срок недель (/set_week) или дату последней менструации (/lmp)."
    if week <= 13:
        return (
            f"📅 Приблизительно {week} неделя первого триместра.\n"
            "Часто: токсикоз, усталость, чувствительность груди. "
            "Пейте воду, маленькими порциями, отдыхайте. Фолиевая кислота — по назначению врача."
        )
    if week <= 27:
        return (
            f"📅 Приблизительно {week} неделя второго триместра.\n"
            "Часто больше сил и аппетит. Контроль веса и давления по рекомендации врача, "
            "лёгкая активность, если это разрешено."
        )
    if week <= 40:
        return (
            f"📅 Приблизительно {week} неделя третьего триместра.\n"
            "Могут быть тренировочные схватки, отёки стоп к вечеру. "
            "Следите за шевелениями по схеме врача, готовьте сумку в роддом."
        )
    return (
        f"📅 Указано {week} недель — обычно беременность до ~40 недель. "
        "Уточните срок с врачом."
    )


