def parse_ru_date(s: str):
    parts = s.strip().split(".")
    if len(parts) != 3:
        return None
    try:
        d, m, y = (int(parts[0]), int(parts[1]), int(parts[2]))
        return Date(y, m, d)
    except (ValueError, OverflowError):
        return None
