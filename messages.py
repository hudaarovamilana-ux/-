def get_random_message(exclude_indices=None):
    """
    Возвращает случайное сообщение из списка, исключая указанные индексы.
    
    Args:
        exclude_indices: список индексов, которые нужно исключить из выбора
    
    Returns:
        tuple: (индекс_сообщения, текст_сообщения)
    """
    import random
    
    if exclude_indices is None:
        exclude_indices = []
    
    available_indices = [i for i in range(len(DAILY_MESSAGES)) if i not in exclude_indices]
    
    if not available_indices:
        # Если все сообщения исключены, возвращаем случайное
        available_indices = list(range(len(DAILY_MESSAGES)))
    
    selected_index = random.choice(available_indices)
    return selected_index, DAILY_MESSAGES[selected_index]
